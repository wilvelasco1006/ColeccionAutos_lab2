public class Auto {
    
    //atributos de la clase auto
    private int anioSerie;
    private String marca;
    private String color;
    private int canTotal;
    private int numSerie;
    
    //constructor
    public Auto() {
    }
    
    //Metodos get y set

    public int getAnioSerie() {
        return anioSerie;
    }

    public void setAnioSerie(int anioSerie) {
        this.anioSerie = anioSerie;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marc) {
        this.marca = marc;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String col) {
        this.color = col;
    }

    public int getCanTotal() {
        return canTotal;
    }

    public void setCanTotal(int canT) {
        this.canTotal = canT;
    }

    public int getNumSerie() {
        return numSerie;
    }

    public void setNumSerie(int numSerie) {
        this.numSerie = numSerie;
    }
    
}

    


    

